    /**
     * @return User[]
     */
    public function findStepValid($id): ?User
    {
        $em = $this->getEntityManager();

        $query = $em->createQuery(
            "SELECT *
                 FROM user_step us
                 INNER JOIN step s ON s.id = us.step_id
                 WHERE us.user_id = :id
                "
        )->setParameter('id', $id);

        return $query->getResult();
    }

